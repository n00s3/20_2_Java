
class Calc {
    public int plus(int num1, int num2) {
        return num1+num2;
    }
    public int minus(int num1, int num2) {
        return num1-num2;
    }
    public double mul(int num1, int num2) {
        return num1*num2;
    }
    public double div(int num1, int num2) {
        return num1/num2;
    }
}